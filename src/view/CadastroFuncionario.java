     public void preenchertabela(){
        FuncionarioDAO dao = new FuncionarioDAO();
        List<Funcionario> lista = dao.listarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaFuncionario.getModel();
        modeloTabela.setRowCount(0);
        for (Funcionario f :  lista){
            
            modeloTabela.addRow(new Object[]{f.getId_funcionario(),f.getNome(),f.getCpf(),f.getEmail(),f.getData_admissao(),f.getTelefone(),f.getSenha()});
        }
    }
    
