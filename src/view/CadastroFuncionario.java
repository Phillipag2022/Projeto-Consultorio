    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int opcao = tabelaFuncionario.getSelectedRow();
        if( opcao >= 0){
            Funcionario p = new Funcionario();
            p.setId_funcionario(Integer.parseInt(tabelaFuncionario.getValueAt(opcao, 0).toString()));
            p.setNome(tabelaFuncionario.getValueAt(opcao, 1).toString());
            p.setCpf(tabelaFuncionario.getValueAt(opcao, 2).toString());
            p.setData_admissao(tabelaFuncionario.getValueAt(opcao, 3).toString());
            p.setEmail(tabelaFuncionario.getValueAt(opcao, 4).toString());
            p.setTelefone(tabelaFuncionario.getValueAt(opcao, 5).toString());
            p.setSenha(tabelaFuncionario.getValueAt(opcao, 6).toString());
            FuncionarioDAO dao = new FuncionarioDAO();
            dao.excluir(p);
            preenchertabela();
            btNovo.setEnabled(true);
            btSalvar.setEnabled(false);
            
        }else{
            JOptionPane.showMessageDialog(null,"Selecione uma linha!!");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

