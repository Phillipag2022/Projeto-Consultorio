    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        int opcao = tabelaConsulta.getSelectedRow();
        if(opcao >= 0){
            Consulta c = new Consulta();
            c.setId_consulta(Integer.parseInt(tabelaConsulta.getValueAt(opcao, 0).toString()));
            c.setData_atendimento(tabelaConsulta.getValueAt(opcao, 1).toString());
            c.setHorario(tabelaConsulta.getValueAt(opcao, 2).toString());
            
            Funcionario f = new Funcionario();
            f.setNome(tabelaConsulta.getValueAt(opcao, 3).toString());
            c.setFuncionario(f);
            
            Medico m = new Medico();
            m.setNome(tabelaConsulta.getValueAt(opcao, 4).toString());
            c.setMedico(m);
            
            Paciente p = new Paciente();
            p.setNome(tabelaConsulta.getValueAt(opcao, 5).toString());
            c.setPaciente(p);
            
            CadastroConsulta cc = new CadastroConsulta(f, c);
            cc.setVisible(true);
        }
    }//GEN-LAST:event_btEditarActionPerformed

