    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int opcao = tabelaConsulta.getSelectedRow();
        if(opcao >= 0){
            Consulta c = new Consulta();
            c.setId_consulta(Integer.parseInt(tabelaConsulta.getValueAt(opcao, 0).toString()));
            c.setData_atendimento(tabelaConsulta.getValueAt(opcao, 1).toString());
            c.setHorario(tabelaConsulta.getValueAt(opcao, 2).toString());
            c.setFuncionario(f);
            c.setMedico(m);
            c.setPaciente(p);
            
            ConsultaDAO dao = new ConsultaDAO();
            dao.excluir(c);
            preenchertabela();
        }
    }//GEN-LAST:event_btExcluirActionPerformed

