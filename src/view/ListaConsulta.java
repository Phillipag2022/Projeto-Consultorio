    public void preenchertabela(){
        ConsultaDAO dao = new ConsultaDAO();
        List<Consulta> lista = dao.listarTodos();
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaConsulta.getModel();
        modeloTabela.setRowCount(0);
        for (Consulta c :  lista){
            
            modeloTabela.addRow(new Object[]{c.getId_consulta(),c.getData_atendimento(),c.getHorario(),c.getFuncionario().getNome(),c.getMedico().getNome(),c.getPaciente().getNome(),c.getFuncionario().getNome()});
        }
    }
    public void preenchertabela(String datapesquisa){
        ConsultaDAO dao = new ConsultaDAO();
        List<Consulta> lista = dao.buscarData(datapesquisa);
        DefaultTableModel modeloTabela = (DefaultTableModel) tabelaConsulta.getModel();
        modeloTabela.setRowCount(0);
        for (Consulta c :  lista){
            
            modeloTabela.addRow(new Object[]{c.getId_consulta(),c.getData_atendimento(),c.getHorario(),c.getFuncionario().getNome(),c.getMedico().getNome(),c.getPaciente().getNome()});
        }
    }
